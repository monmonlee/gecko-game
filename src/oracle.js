const pick = arr => arr[(Math.random() * arr.length) | 0];

// 抽一支完整的籤：一句喃喃 ＋ 今日直覺（避開最近抽過的喃喃）
export function drawFortune(recent = []) {
  const pool = ORACLE_LINES.filter(l => !recent.includes(l));
  const from = pool.length ? pool : ORACLE_LINES;
  return {
    line: from[(Math.random() * from.length) | 0],
    color: pick(LUCKY_COLORS),
    number: 1 + ((Math.random() * 9) | 0),   // 1–9
    tip: pick(LUCKY_TIPS),
  };
}
